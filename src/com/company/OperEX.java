package com.company;

public class OperEX {

    public static void main(String[] args)
    {
        int i = 10;
        int j = 20;
        System.out.println(i+j);
        System.out.println(j-i);
        System.out.println(j/i);
        System.out.println(j%i);
        //increment and decrement operators
        System.out.println(i*j);
        System.out.println(i++);
        System.out.println(++i);
        System.out.println(j--);
        System.out.println(--j);
    }
}


